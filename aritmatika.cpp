#include<iostream>

using namespace std;

int main(){

    int a = 9;
    int b = 3;

    int hasil;

    //penjumlahan
    hasil = a + b;
    cout << a << " + " << b << " = " << hasil << endl;

    //pengurangan
    hasil = a - b;
    cout << a << " - " << b << " = " << hasil << endl;

    //pembagian
    hasil = a / b;
    cout << a << " / " << b << " = " << hasil << endl;

    //perkalian
    hasil = a * b;
    cout << a << " x " << b << " = " << hasil << endl;

    //modulus
    hasil = a % b;
    cout << a << " % " << b << " = " << hasil << endl;

    return 0;
}
